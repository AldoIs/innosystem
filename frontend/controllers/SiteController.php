    }
       public function actionProfile()
    {
        $model = null;
        $model = Profile::find()->where(['id_user' => Yii::$app->user->id])->one();
        if($model == null) {
           $model = new Profile();  
        }
        return $this->render('profile' , [
            'model' => $model,
        ]);
    
    }
}
